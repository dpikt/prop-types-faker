import jsc from 'jsverify';
import { getRandomness } from './randomness';

/**
 * Given some jsverify arb, return the first value. It
 * uses the global randomness.
 * @param {*} arb
 */
export function takeSample(arb) {
  return jsc.sampler(arb, getRandomness())(1)[0];
}

export function createGenerator(element) {
  return jsc.bless({
    generator: jsc.generator.bless(() => element),
  });
}

